    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       String skilName = txtSkillName.getText();
       Skill skill=null;
        if(skilName != null && !skilName.trim().isEmpty()){
            skill=new Skill(0, skilName);
            skillDao.insertSkill(skill);
        }else {
        skill =(Skill) cbSkill.getSelectedItem();
        }
        int power = sliPower.getValue();
        UserSkill us = new UserSkill(null, Config.loggedInUser, skill, power);
        userSkill.insertUserSkill(us);
        
        fillWindow();
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
      int index = tblSkills.getSelectedRow();
      UserSkill skill=userSkills.get(index);
      userSkill.removeUserSkill(skill.getId());
      fillWindow();
      
    }//GEN-LAST:event_btnDeleteActionPerformed


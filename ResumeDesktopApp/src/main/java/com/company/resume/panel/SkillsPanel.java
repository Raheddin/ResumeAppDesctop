    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        
       UserSkill selectUserSkill = userSkills.get(tblSkills.getSelectedRow());
       String skilName = txtSkillName.getText();
       Skill skill=null;
        if(skilName != null && !skilName.trim().isEmpty()){
            skill=new Skill(0, skilName);
            skillDao.insertSkill(skill);
        }else {
        skill =(Skill) cbSkill.getSelectedItem();
        }
        int power = sliPower.getValue();
        selectUserSkill.setPower(power);
        selectUserSkill.setSkill(skill);
        
        userSkill.updateUserSkill(selectUserSkill);
        fillWindow();
    }//GEN-LAST:event_btnSaveActionPerformed


